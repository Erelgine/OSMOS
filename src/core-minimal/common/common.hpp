#ifndef COMMON_H
#define COMMON_H

// Common type definitions. Very useful and necessary for almost every function.
typedef unsigned char		uint_8;					// Unsigned 8-bit integer
typedef unsigned short		uint_16;				// Unsigned 16-bit integer
typedef unsigned int		uint_32;				// Unsigned 32-bit integer
typedef unsigned long		uint_64;				// Unsigned 64-bit integer

typedef signed char			int_8;					// Signed 8-bit integer
typedef signed short		int_16;					// Signed 16-bit integer
typedef signed int			int_32;					// Signed 32-bit integer
typedef signed long			int_64;					// Signed 64-bit integer

#endif